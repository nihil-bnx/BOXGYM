package DATOS;
public class Cliente {
    String nombre;
    String apellidos;
    String DNI;
    String telefono;

    public Cliente() {
    }

    public Cliente(String nombre, String apellidos, String DNI, String telefono) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.DNI = DNI;
        this.telefono = telefono;
    }
    
    
    
    
}
